package testePratica;

public class Funcionario {
	private String nome;
	
	
	public Funcionario(String nome) {
		super();
		this.nome = nome;
	}

	public Funcionario() {
		super();
		this.nome = "";
	}

	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}

}	
	